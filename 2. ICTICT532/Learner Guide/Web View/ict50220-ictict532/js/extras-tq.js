$(document).ready(function() {
  var first = true;

  $(".bespoke3-box").hover(function() {
    $(this).find("i").animate({
      'marginRight': '15px'
    }, {
      duration: 300,
      queue: false
    })

  }, function() {
    $(this).find("i").animate({
      'marginRight': '0px'
    }, {
      duration: 300,
      queue: false
    })
  })

  $(".bespoke3-box").click(function() {
    animateIn($(this))
    $(".bespoke3-text-box").css("borderColor", $(this).css("backgroundColor"))
  })

  $(".bespoke3-box").on("keydown", function(e) {
    if (e.which == 13)
      animateIn($(this))
  })

  function animateIn(ctx) {
    $(".bespoke3-text-box").children().fadeOut("fast", function() {
      $(this).html("")
      $(this).html(ctx.data("text"))
      set()
    })

    function set() {
      if (first) {
        if (ctx.data('pos') == 'bot') {
          $(".bespoke3-text-box").children().css('marginTop', '15px')
        } else {
          $(".bespoke3-text-box").children().css('marginTop', '-15px')
        }
        animate()
        first = false;
      } else {
        $(".bespoke3-box").animate({
          'top': '0px'
        }, {
          duration: 200,
          queue: false,
          complete: function() {
            if (ctx.data('pos') == 'bot') {
              $(".bespoke3-text-box").children().css('marginTop', '15px')
            } else {
              $(".bespoke3-text-box").children().css('marginTop', '-15px')
            }
            animate()
          }
        })
      }
    }

    function animate() {
      if (ctx.data('pos') == 'bot') {
        $(".bespoke3-text-box").children().animate({
          'marginTop': '0px'
        }, {
          duration: 300,
          queue: false
        })
        ctx.animate({
          'top': '-15px'
        }, {
          duration: 300,
          queue: false
        })
      } else {
         $(".bespoke3-text-box").children().animate({
          'marginTop': '0px'
        }, {
          duration: 300,
          queue: false
        })
        ctx.animate({
          'top': '15px'
        }, {
          duration: 300,
          queue: false
        })
      }
      $(".bespoke3-text-box").children().fadeIn("fast")
    }
  }
})

